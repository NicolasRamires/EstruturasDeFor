package ForClassicEInvertido;

public class ForClassicoInvertido {
    public static void main(String[] args){

        //For Clássico Invertido
        System.out.println("Agora o valor será invertido até o valor 0");

        for(int b=10; b>=0; b--){
            System.out.println("O valor da B é " + b);
        }
    }
}
